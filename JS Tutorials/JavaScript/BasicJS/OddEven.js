function checkOddOrEven(number) {
    if (number % 2 === 0) {
      console.log(number + " is even.");
    } else {
      console.log(number + " is odd.");
    }
  } 
  // Example usage
  checkOddOrEven(4);  
  checkOddOrEven(7);  
  checkOddOrEven(10); 

